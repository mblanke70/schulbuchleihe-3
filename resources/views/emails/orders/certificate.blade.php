# Liebe Eltern,

wir möchten Sie noch einmal daran erinnern, dass Familien mit drei oder mehr schulpflichtigen Kindern das Recht auf einen Nachlass der Gebühren für die geliehenen Schulbücher um 20 % haben.

Sollten Sie einen Anspruch auf diesen Nachlass haben, so bitten wir Sie, zu überprüfen, ob Sie eine Schulbescheinigung bei uns eingereicht haben. Nur mit den aktuellen Schulbescheinigungen der Geschwisterkinder können wir diesen Nachlass gewähren.

(Sind die Geschwisterkinder an der Ursulaschule, so benötigen wir KEINE Bescheinigungen. Unsere eigenen Schüler und Schülerinnen kennen wir!)

Noch fehlende Schulbescheinigungen können Sie bis Freitag, den 11.9.2020, im Sekretariat nachreichen.

Mit freundlichen Grüßen<br>
